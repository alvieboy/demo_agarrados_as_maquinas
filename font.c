static unsigned __fontseg_spacing(const struct font *font, unsigned char c)
{
    unsigned x;
    if (c=='.') {
        x=14;
    } else {
        x=font->w;
    }
    return x;

}
static unsigned __font1616_spacing(const struct font *font, unsigned char c)
{
    unsigned x;
    if (c=='.') {
        x=6;
    } else {
        x=font->w;
    }
    return x;

}

